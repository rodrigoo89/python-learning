mi_lista = ["a", "b", "c", "d", "e" ] # Crea una lista con los elementos "a", "b", "c", "d" y "e"
print(type(mi_lista)) # Imprime el tipo de la variable mi_lista
mi_lista.append("f") # Agrega el elemento "f" al final de la lista
print(mi_lista) # Imprime la lista completa
mi_lista.reverse() # Invierte el orden de los elementos en la lista
print(mi_lista) # Imprime la lista invertida    
mi_lista.sort() # Ordena los elementos de la lista en orden alfabético
print(mi_lista) # Imprime la lista ordenada 
mi_lista.pop() # Elimina el último elemento de la lista
print(mi_lista) # Imprime la lista después de eliminar el último elemento       
print(mi_lista[0]) # Imprime el primer elemento de la lista, que es "a"
